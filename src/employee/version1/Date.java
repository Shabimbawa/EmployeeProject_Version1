package employee.version1;

/**
 *
 * @author User
 */
public class Date{
    private int year;
    private int month;
    private int day;

    public Date(int year, int month, int day) {
        this.year = year;
        this.month = month;
        this.day = day;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public void setMonth(int month) {
        this.month = month;
    }

    public void setDay(int day) {
        this.day = day;
    }

    public int getYear() {
        return year;
    }

    public int getMonth() {
        return month;
    }

    public int getDay() {
        return day;
    }
    
        @Override
    public String toString() {
        return String.format("%04d-%02d-%02d", year, month, day); // Format as YYYY-MM-DD
    }
}
